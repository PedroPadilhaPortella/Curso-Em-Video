frase = '   meu nome e pedro portella    '.strip()

print(frase.count("o"))
print(len(frase))
print(frase.replace('pedro', 'daniel'))
print(frase.find('portella'))
print(frase.split(' '))

# len() => retorna o comprimento da string
# count() => contaalgo
# find() => procura algo
# replace() => troca de caracteres de strings
# upper() => maiusculo
# lower() => minusculo
# capitalize() => primeira letra da
# title() => primeira letra maiuscula
# strip() => remove espacos inuteis de strings, tambme tem lstrip() e rstrip()
# join() => junta strings ou caracteres ou arrays com um parametro, que pode ser um espaco, \n ou hifen
# spit() => divide uma string em um determinado parametro, como espacos, \n ou hifen
